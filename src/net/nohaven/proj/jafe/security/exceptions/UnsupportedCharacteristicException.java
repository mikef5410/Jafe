package net.nohaven.proj.jafe.security.exceptions;

public class UnsupportedCharacteristicException extends Exception {
    private static final long serialVersionUID = 9085959653155744429L;

    public UnsupportedCharacteristicException() {
        super();
    }

    public UnsupportedCharacteristicException(String arg0) {
        super(arg0);
    }

    public UnsupportedCharacteristicException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public UnsupportedCharacteristicException(Throwable arg0) {
        super(arg0);
    }

}
